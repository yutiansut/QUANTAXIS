def QA_util_date_gap(date, gap, methods):
    """
    explanation:
        ËøîÂõûstart_dayÂà∞end_day‰∏≠Èó¥ÊúâÂ§öÂ∞ë‰∏™‰∫§ÊòìÂ§© ÁÆóÈ¶ñÂ∞æ

    params:
        * date->
            Âê´‰πâ: Â≠óÁ¨¶‰∏≤Ëµ∑ÂßãÊó•
            Á±ªÂûã: str
            ÂèÇÊï∞ÊîØÊåÅ: []
        * gap->
            Âê´‰πâ: Èó¥ÈöîÂ§öÊï∞‰∏™‰∫§ÊòìÊó•
            Á±ªÂûã: int
            ÂèÇÊï∞ÊîØÊåÅ: [int]
        * methods->
            Âê´‰πâ: ÊñπÂêë
            Á±ªÂûã: str
            ÂèÇÊï∞ÊîØÊåÅ: ["gt->Â§ß‰∫é", "gte->Â§ß‰∫éÁ≠â‰∫é","Â∞è‰∫é->lt", "Â∞è‰∫éÁ≠â‰∫é->lte", "Á≠â‰∫é->==="]
    """
    try:
        if methods in [">", "gt"]:
            return trade_date_sse[trade_date_sse.index(date) + gap]
        elif methods in [">=", "gte"]:
            return trade_date_sse[trade_date_sse.index(date) + gap - 1]
        elif methods in ["<", "lt"]:
            return trade_date_sse[trade_date_sse.index(date) - gap]
        elif methods in ["<=", "lte"]:
            return trade_date_sse[trade_date_sse.index(date) - gap + 1]
        elif methods in ["==", "=", "eq"]:
            return date

    except:
        return "wrong date"


