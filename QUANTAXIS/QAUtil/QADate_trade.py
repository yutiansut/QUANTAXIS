def QA_util_get_real_date(date, trade_list=trade_date_sse, towards=-1):
    """
    explanation:
        Ëé∑ÂèñÁúüÂÆûÁöÑ‰∫§ÊòìÊó•Êúü

    params:
        * date->
            Âê´‰πâ: Êó•Êúü
            Á±ªÂûã: date
            ÂèÇÊï∞ÊîØÊåÅ: []
        * trade_list->
            Âê´‰πâ: ‰∫§ÊòìÂàóË°®
            Á±ªÂûã: List
            ÂèÇÊï∞ÊîØÊåÅ: []
        * towards->
            Âê´‰πâ: ÊñπÂêëÔºå 1 -> ÂêëÂâç, -1 -> ÂêëÂêé
            Á±ªÂûã: int
            ÂèÇÊï∞ÊîØÊåÅ: [1Ôºå -1]
    """
    date = str(date)[0:10]
    if towards == 1:
        if pd.Timestamp(date) >= pd.Timestamp(trade_list[-1]):
            return trade_list[-1]
        while date not in trade_list:
            date = str(
                datetime.datetime.strptime(str(date)[0:10], "%Y-%m-%d")
                + datetime.timedelta(days=1)
            )[0:10]
        else:
            return str(date)[0:10]
    elif towards == -1:
        if pd.Timestamp(date) <= pd.Timestamp(trade_list[0]):
            return trade_list[0]
        while date not in trade_list:
            date = str(
                datetime.datetime.strptime(str(date)[0:10], "%Y-%m-%d")
                - datetime.timedelta(days=1)
            )[0:10]
        else:
            return str(date)[0:10]


